    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        if (GlobalMethods.isEmpty(txtSenderName.getText()) ||
                GlobalMethods.isEmpty(txtSenderAddress.getText()) ||
                GlobalMethods.isEmpty(txtRecipientName.getText()) ||
                GlobalMethods.isZero(txtLength.getText()) ||
                GlobalMethods.isZero(txtWidth.getText()) ||
                GlobalMethods.isZero(txtHeight.getText()) ||
                GlobalMethods.isZero(txtWeight.getText())) {
            if (GlobalMethods.isEmpty(txtSenderName.getText()) ||
                GlobalMethods.isEmpty(txtSenderAddress.getText()) ||
                GlobalMethods.isEmpty(txtRecipientName.getText())) {
                JOptionPane.showMessageDialog(null, "There Are Empty Fields", "Unable to Create", JOptionPane.ERROR_MESSAGE);
            }
            else {
                JOptionPane.showMessageDialog(null, "There Are Zero Values", "Unable to Create", JOptionPane.ERROR_MESSAGE);
            }
        }
        else {
            updatePrice();
        
            int reply = JOptionPane.showConfirmDialog(null, "Confirm Order?", "Confirm Order", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                ord.setCustomer(cust.getEmail());
                ord.setPrice(Double.parseDouble(txtPrice.getText()));
                ord.setSenderName(txtSenderName.getText());
                ord.setSenderAddress(txtSenderAddress.getText());
                ord.setRecipientName(txtRecipientName.getText());
                ord.setRecipientAddress(txtRecipientAddress.getText());
                Dimension dim = new Dimension(Double.parseDouble(txtLength.getText()), Double.parseDouble(txtWidth.getText()), Double.parseDouble(txtHeight.getText()));
                ord.setDimensions(dim);
                ord.setWeight(Double.parseDouble(txtWeight.getText()));
                ord.setPickupDate(cldPickupDate.getDate());
                ord.setStatus(Status.AWAITING_CONFIRMATION);
                
                ord.create();
            
                JOptionPane.showMessageDialog(null, "Order Succesfully Created", "Order Created", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        
        
    }//GEN-LAST:event_btnCreateActionPerformed

