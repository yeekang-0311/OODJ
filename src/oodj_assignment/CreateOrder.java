    private void updatePrice() {
        double l = Double.parseDouble(txtLength.getText());
        double w = Double.parseDouble(txtWidth.getText());
        double h = Double.parseDouble(txtHeight.getText());
        Dimension dim = new Dimension(l, w, h);
        double weight = Double.parseDouble(txtWeight.getText());
        
        txtPrice.setText(String.format("%.2f", ord.calcPrice(dim, weight)));
    }
    
