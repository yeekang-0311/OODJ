    private boolean checkDuplicateEmail(String email) {
        try (BufferedReader br = new BufferedReader(new FileReader("txtfiles/Users.txt"))) 
            {
                Object records[] = br.lines().toArray();
                for(int index=0;index<records.length;index++){
                    String eachline = records[index].toString();
                    String []row = eachline.split(",");
                    if(row[1].equals(email)) {  
                        return true;
                    }      
                }

            } catch (Exception e){
                e.printStackTrace();
            }
        return false;
    }
    
    private String hashPassword(String password) {
        
        try {
            /* Uses MD5 hashing */
            MessageDigest md = MessageDigest.getInstance("MD5");
            byte[] messageDigest = md.digest(password.getBytes()); 
            
            /* Convert byte array into signum representation */
            BigInteger no = new BigInteger(1, messageDigest); 
  
            /* Convert message digest into hex value */
            String hashtext = no.toString(16); 
            while (hashtext.length() < 32) { 
                hashtext = "0" + hashtext; 
            } 
  
            return hashtext;
            
        } catch (NoSuchAlgorithmException e){        
            
            System.out.print(e);
            return null;
        }
    }
    
    private void saveData() {
        FileWriter file = null;
        PrintWriter pws = null;
        try
        {
            file = new FileWriter("txtfiles/Users.txt",true);
            pws = new PrintWriter(file);      

            pws.println(txtName.getText() + "," + txtEmail.getText() + "," 
                    + hashPassword(txtPassword.getText()) + "," + comboRole.getSelectedItem());
                
           
            JOptionPane.showMessageDialog(rootPane, "User Account Added Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            clearText();

           
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, e, "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        finally {           
            if (pws != null) {
                pws.flush();
                pws.close(); 
            }
            
        }
    }
    
    private void clearText() {
        txtName.setText("");
        txtEmail.setText("");
        txtPassword.setText("");
        comboRole.setSelectedIndex(0);
    }

