    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        InputValidator iv = new InputValidator();
        if(iv.isEmpty(txtName.getText()) 
                || iv.isEmpty(txtEmail.getText()) 
                || iv.isEmpty(txtPassword.getText())){
            JOptionPane.showMessageDialog(rootPane, "Empty values in name, email or password", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(comboRole.getSelectedItem().equals("Select a role")) {
            JOptionPane.showMessageDialog(rootPane, "Please select a role", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(!iv.isLettersOnly(txtName.getText())){
            JOptionPane.showMessageDialog(rootPane, "Name input is invalid", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else {
            if(checkDuplicateEmail(txtEmail.getText())){
                JOptionPane.showMessageDialog(rootPane, "Account existed, please use another email", "Email duplicated", JOptionPane.ERROR_MESSAGE);
            }else {
                saveData();
            }
            
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed

